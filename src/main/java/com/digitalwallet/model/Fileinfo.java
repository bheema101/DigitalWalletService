package com.digitalwallet.model;

import java.io.Serializable;
import java.time.LocalDateTime;
import java.util.Set;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBMapperFieldModel.DynamoDBAttributeType;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTypeConverted;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTyped;
import com.amazonaws.services.dynamodbv2.datamodeling.JsonMarshaller;
import com.digitalwallet.converter.LocalDateTimeToStringTypeConverter;
import com.digitalwallet.converter.SetConverter;

@DynamoDBTable(tableName = "User_Files")
public final class Fileinfo implements Serializable{

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String id;
    private String tuid;
    @DynamoDBTypeConverted(converter = SetConverter.class)
    private Set<String> fileNames;
    private String pnr;
    private String tripId;
    @DynamoDBTypeConverted(converter = LocalDateTimeToStringTypeConverter.class)
    @DynamoDBTyped(DynamoDBAttributeType.S)
    private LocalDateTime uploadedTime;
    
    
    
    
    
    
    
    public Fileinfo() {
		super();
	}
	public Fileinfo( String tuid, Set<String> fileNames, String pnr, String tripId, LocalDateTime uploadedTime) {
		super();
		this.tuid = tuid;
		this.fileNames=fileNames;
		this.pnr = pnr;
		this.tripId = tripId;
		this.uploadedTime = uploadedTime;
	}
   
	@DynamoDBAutoGeneratedKey
	@DynamoDBHashKey
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	@DynamoDBAttribute(attributeName = "tuid")
	public String getTuid() {
		return tuid;
	}
	public void setTuid(String tuid) {
		this.tuid = tuid;
	}
	@DynamoDBAttribute(attributeName = "pnr")
	public String getPnr() {
		return pnr;
	}
	public void setPnr(String pnr) {
		this.pnr = pnr;
	}
	@DynamoDBAttribute(attributeName = "tripId")
	public String getTripId() {
		return tripId;
	}
	public void setTripId(String tripId) {
		this.tripId = tripId;
	}
	@DynamoDBAttribute(attributeName = "uploadedTime")
	public LocalDateTime getUploadedTime() {
		return uploadedTime;
	}
	public void setUploadedTime(LocalDateTime uploadedTime) {
		this.uploadedTime = uploadedTime;
	}
	
	@DynamoDBAttribute(attributeName = "fileNames")
	public Set<String> getFileNames() {
		return fileNames;
	}
	public void setFileNames(Set<String> fileNames) {
		this.fileNames = fileNames;
	}
    
    
    
}
